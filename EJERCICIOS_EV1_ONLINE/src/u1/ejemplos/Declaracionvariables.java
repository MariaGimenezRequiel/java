package u1.ejemplos;

public class Declaracionvariables {

	public static void main(String[] args) {
		
		// Declaro e inicializo las dos primeras variables numéricas
		int numero=8 ;
		double decimal= 9485.58172;
		
		// Imprimo las variables
		System.out.println(numero);
		System.out.println(decimal);
		
		
		
        //Declaro las siguientes variables de tipo string
		String verdad ="true";
		String frase ="Esto es un ejemplo de tipo de dato";
		
		// Imprimo las variables de tipo "string"
		System.out.println(verdad); 
	    System.out.println(frase);
	    
	    
	    
	    
		// Declaro valor numérico decimal negativo
		double negativo= -135.248053912; 
		
		//Imprimo el valor declarado.out.println(negativo);
		System.out.println(negativo); 
		
		
		 
	    //Declaro variable de tipo "char"
		char letra='w'; 
	
		// Imprimo variables de tipo "char"
		System.out.println(letra);
		

		
		// Imprimo las siguientes declaraciones

		System.out.println("Mi úmero de cliente es:\t"+ "12345" );
		   
		System.out.println("Mi nombre es:\t" + "\"Pepe\"");
		    
		    
		    
		// TODO Auto-generated method stub

	}

}
