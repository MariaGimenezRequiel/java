package u3.tareas;

public class Tarea7 {

	public static void main(String[] args) {
		/*
		 * Hacer una aplicación que muestre todos los números del 100 al 1 de 3 en 3.
		 * (Ejemplo: 100 - 97 - 94 - ... - 1)
		 */
		System.out.println("Números de 100 a 0 de 3 en 3.");
		for(int n=100; n>=0; n=n-3) {
			System.out.println(n);
			
		}
	}

}
